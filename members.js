const sorter = document.getElementById('sort-list')

sortData = () => {
  const sortBy = sorter.value
  membersNew.sort((a,b) => {
    // If sorting by date, extract the year part for comparison
    if (sortBy === 'date') {
        const yearA = parseInt(a[sortBy])
        const yearB = parseInt(b[sortBy])
        if (yearA < yearB) return -1;
        if (yearA > yearB) return 1;
        return 0;
    } else {
       switch (sortBy) {
          case "firstName":
            return a.firstname.localeCompare(b.firstname);
          case "lastName":
            return a.lastname.localeCompare(b.lastname);
          default:
            console.log('data sorted')
            return a.role - b.role;
       }
      }   
  })

  showSortedData(membersNew)
}





